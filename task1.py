def caching_fibonacci():
    """
    Створює функцію fibonacci з кешем для зберігання результатів.
    Використовує замикання — внутрішня функція пам'ятає cache.

    :return: функцію fibonacci(n)
    """
    # Словник для зберігання вже обчислених значень
    cache = {}
    def fibonacci(n):
        """
        Обчислює n-е число Фібоначчі рекурсивно з кешуванням.

        :param n: номер числа у послідовності
        :return: n-е число Фібоначчі
        """
        # Базові випадки: F(0) = 0, F(1) = 1
        if n <= 0:
            return 0
        if n == 1:
            return 1
        # Якщо значення вже є в кеші — повертаємо його без обчислення
        if n in cache:
            return cache[n]
        # Обчислюємо рекурсивно і зберігаємо в кеш
        cache[n] = fibonacci(n - 1) + fibonacci(n - 2)
        return cache[n]
    # Повертаємо функцію fibonacci — вона "пам'ятає" cache через замикання
    return fibonacci

# Приклад використання
# Отримуємо функцію fibonacci
fib = caching_fibonacci()

# Використовуємо для обчислення чисел Фібоначчі
print(fib(10))  # Виведе 55
print(fib(15))  # Виведе 610