import re
def generator_numbers(text: str):
    """
    Генератор що знаходить всі дійсні числа в тексті.
    Числа мають бути відокремлені пробілами з обох боків.

    :param text: рядок з текстом
    :yield: дійсні числа типу float по одному
    """
    # Шаблон для пошуку дійсних чисел відокремлених пробілами
    pattern = r'(?<= )\d+\.\d+(?= )'
    # Знаходимо всі числа що відповідають шаблону
    matches = re.findall(pattern, text)
    # Повертаємо кожне число по одному через yield
    for match in matches:
        yield float(match)
def sum_profit(text: str, func):
    """
    Підраховує загальну суму дійсних чисел у тексті.

    :param text: рядок з текстом
    :param func: функція-генератор для пошуку чисел
    :return: загальна сума знайдених чисел
    """
    # Передаємо текст у генератор і підсумовуємо всі знайдені числа
    return sum(func(text))

# Приклад використання
text = "Загальний дохід працівника складається з декількох частин: 1000.01 як основний дохід, доповнений додатковими надходженнями 27.45 і 324.00 доларів."
total_income = sum_profit(text, generator_numbers)
print(f"Загальний дохід: {total_income}")
